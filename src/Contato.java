import java.util.*;

class Contato {
    String nome;
    String telefone;
    String nascimento;
    String email;
    String celular;

    public Contato(String nome, String telefone, String nascimento, String email, String celular) {
        this.nome = nome;
        this.telefone = telefone;
        this.nascimento = nascimento;
        this.email = email;
        this.celular = celular;
    }
}

